public class MultiplyFunction{

	public int computeMultiply(int number1, int number2 ){
		if(number1 < 1 || number2 > 1)
		for(int counter = 1; counter <= number1; counter++){
			int multiply = number1 + counter;
		}
		if(number2 > 1 || number2 < 1)
		for(int counter = 1; counter >= number2; counter++){
			int multiply = number2 + counter;
			
		}
			return multiply;
	}
}
